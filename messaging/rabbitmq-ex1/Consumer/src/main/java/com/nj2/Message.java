package com.nj2;


public class Message {

	private String title = null;
	private String body = null;

	public void setTitle( String title ) {
		this.title = title;
	}

	public void setBody( String body ) {
		this.body = body;
	}

	public String  getTitle() {
		return this.title;
	}

	public String getBody() {
		return this.body;
	}
}

